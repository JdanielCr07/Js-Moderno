//OPERACIONES NUMERICAS EN JS

const numero = 20;
const numero1 = 30;

let resultado;

//Suma
resultado = numero1 + numero;

//Resta
resultado = numero1 - numero;

//Multiplicacion
resultado = numero1 * numero;

//division
resultado = numero1 / numero;

//modulo
resultado = numero1 % numero;

console.log(resultado);